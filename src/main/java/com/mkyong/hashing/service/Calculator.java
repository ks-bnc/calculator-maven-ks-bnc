package com.mkyong.hashing.service;

public class Calculator {
    public double osszead(double op1, double op2) {
        return op1 + op2;
    }
    public  double kivon (double op1, double op2){
        return op1 - op2;
    }
    public double szoroz (double op1, double op2){
        return op1 * op2;
    }
    public double oszt (double op1, double op2){
        return op1 / op2;
    }
}
